

function mostrarMais1(){
   
    let pontos  = document.querySelector(".pontos1") 
    let maisTexto  = document.querySelector(".mais1")
    let btnLeiaMais = document.querySelector(".btnMais1")

    if(maisTexto.style.opacity != "1"){
        pontos.style.opacity = "1";
        maisTexto.style.opacity = "1";
        btnLeiaMais.innerHTML ="Leia menos";
    }else{
        pontos.style.opacity = "1";
        maisTexto.style.opacity = "0";
        btnLeiaMais.innerHTML = "Leia Mais";
    } 
}

